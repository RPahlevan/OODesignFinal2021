package common;

/**
 * Enum to specify procedure options used in event passing.
 * The values are meant to provide a specific insight into
 * the procedure that is currently taking place.
 *
 * @author ebreojh
 */
public enum ProcedureOptions {
    FULL,
    SETUP,
    RELEASE,
    ACTIVATE,
    DEACTIVATE,
    RU,
    PARAM,
    ALARM,
    CARRIER,
    SCALING,
    POST,
    DIAGNOSTIC,
    RAT_TYPE
}

